<blockquote>
Absolutely! Come to the registration table to sign up for classes that day.
</blockquote>

<a name="fees"></a>
<br/><hr/><br/>

<i>A class I want to take has a fee. To whom do I pay it and when?</i>

<blockquote>
All fees for class materials and/or handouts are paid to the instructor of the class at the time of the class. 
<br/><br/>
Please send no money to the Registrar. She'll just return it to you at the site.
</blockquote>

<a name="teach"></a>
<br/><hr/><br/>

<i>I'd like to teach a class. What do I need to do to get it into the course catalog?</i>

<blockquote> 
Contact the <a href="functions/mailto.php?u=university&amp;d=atlantia.sca.org" target="redir">Chancellor of the University</a> 
with the name of the class you wish to teach, a brief description, how long the class will take (in hours), 
if there are any material fees (to cover handouts or items your students may need), 
if there is a maximum number of students your class can accommodate, as well as your name and contact information. 
</blockquote>

<a name="degree"></a>
<br/><hr/><br/>

<i>How do I earn a degree?</i>

<blockquote>
There are four degrees given out by the University of Atlantia.<br/><br/>
<b>A Bachelor's Degree</b> is awarded after a student has completed 26 hours of classes.<br/><br/>
<b>A Fellowship</b> in the University of Atlantia is awarded to those who have taught classes at three universities.<br/><br/>
<b>A Masters Degree</b> is awarded to those who have earned their Bachelor's and are also Fellows of the University.<br/><br/>
<b>Honorary Doctorates</b> are awarded by the Chancellor at his or her discretion.</blockquote>

<a name="degree2"></a>
<br/><hr/><br/>

<i>I'm pretty sure I should have earned a degree by now. Why haven't I received one?</i>

<blockquote> 
If you believe you should have earned a degree as stated by the requirements above, contact the 
<a href="functions/mailto.php?u=registrar&amp;d=atlantia.sca.org" target="redir">registrar</a>.  
Please note that if you have changed your mundane or SCA names since the first university you attended, 
your records may be filed under two separate names unless you've informed the registrar. 
The University database is independent from all other SCA membership records. 
<br/><br/>
Class attendance is recorded by the roster sheets turned in by the instructor after the class. 
Make certain that if you have attended at least 75% of a class that you have <i>legibly</i> signed the roster in order to receive credit. 
</blockquote>

<a name="handout"></a>
<br/><hr/><br/>

<i>I'd like a handout for a class I can't attend. How can I get one?</i> 

<blockquote>
If you are able to attend the University session where the class is being taught, drop by and ask the instructor if there's an extra you can take. 
If there isn't, most instructors are happy to get another copy made for you. <br/>
<br/>
If you will not be at the session, ask the <a href="functions/mailto.php?u=university&amp;d=atlantia.sca.org" target="redir">Chancellor</a> 
to put you in touch with the instructor. 
</blockquote>
